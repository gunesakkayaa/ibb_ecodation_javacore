package com.gunesakkaya._1_part_javatypes;

public class _07_EscapeCharacter {

    public static void main(String[] args) {
        System.out.println("Gunes, Akkaya!"); // Normal metin
        System.out.println("Gunes,\nAkkaya!"); // Yeni satır (\n)
        System.out.println("Gunes,\tAkkaya!"); // Tab boşluğu (\t)
        System.out.println("Gunesin \"Java\" dili!"); // Çift tırnak (\")
        System.out.println("Onun\'Agatha kitapları!"); // Tek tırnak (\')
        System.out.println("C:\\Users\\Gunes\\Desktop"); // Ters eğik çizgi (\\)
    }
}
